# Creating different variables about the user
name = "Shad"
age = 23
state = "Washington"

# Printing in readable format using f-string
print(f"My name is {name}, I am {age} years old, and I live in {state}.")

# Printing user results in a readable format using .format() method
print("My name is {}, I am {} years old, and I live in {}.".format(name, age, state))

# Printing user results in a readable format using string concatenation
print("My name is " + name + ", I am " + str(age) + " years old, and I live in " + state + ".")