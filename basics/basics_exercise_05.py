# Practice with str methods and basic transformations
sentence = "python is awesome!"

# Convert sentence to uppercase for emphasis
uppercase_sentence = sentence.upper()

# Convert sentence to lowercase for consistency
lowercase_sentence = sentence.lower()

# Convert sentence to title case (Capitalizes the first letter of every word)
title_case_sentence = sentence.title()

# Get total number of characters in the sentence
count_sentence_characters = len(sentence)

# Display all different string transformations
print(uppercase_sentence)
print(lowercase_sentence)
print(title_case_sentence)
print(count_sentence_characters)

