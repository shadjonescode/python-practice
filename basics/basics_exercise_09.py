# Creating two integer variables to use in division operations
a = 17
b = 4

# Find the remainder when a is divided by b using modulo operator
remainder = a % b

# Perform floor division to get the whole-number result of a divided by b
floor_division = a // b

# Print both results so we can compare remainder vs floor division
print(f"Remainder: {remainder}")
print(f"Floor division: {floor_division}")